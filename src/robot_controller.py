class RobotMovements:
    """
    Basic Movements
    """

    def avanzar(self):
        print("Robot avanzando")

    def retroceder(self):
        print("Robot retrocediendo")

    def girar_izquierda(self):
        print("Robot girando a la izquierda")

    def girar_derecha(self):
        print("Robot girando a la derecha")

    def detener(self):
        print("Robot detenido")

    def abrir_pinza(self):
        print("Abriendo pinza")

    def cerrar_pinza(self):
        print("Cerrando pinza")

    def subir_brazo(self):
        print("Subiendo brazo")

    def bajar_brazo(self):
        print("Bajando brazo")
